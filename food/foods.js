// Food database: ~140 common foods with per-100 g macros and a natural
// serving. Values are USDA-flavored approximations — the product goal is
// accountability, not lab accuracy (see parser.js).
//
// Entry shape:
//   name     canonical display name
//   aliases  other spoken names that should match
//   serving  { label, grams } — what "1 <food>" or "a serving" means
//   per100   { kcal, p, c, f } — per 100 g
//   cup/tbsp/tsp  grams per volume unit, where volume logging makes sense
//   liquid   true → generic glass/ml handling applies

function F(name, aliases, servingLabel, servingGrams, kcal, p, c, f, extra = {}) {
  return {
    name,
    aliases,
    serving: { label: servingLabel, grams: servingGrams },
    per100: { kcal, p, c, f },
    ...extra,
  };
}

